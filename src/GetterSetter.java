
public class GetterSetter {
	
	//Demonstrate Getter Setter concept in java
	//Used when on has to play around with private Variables
	
	private String name ="Rahul Tripathi";
	
	//Setter
	public void setName(String somename) {
		this.name = somename;
	}
	
	//Getter
	public String getName() {
		return this.name;
	}
	

}
